#!/usr/bin/env python3
'''
    Function that takes a string str1 and a string str2
    as arguments and returns their concatenation
'''


def concat(str1: str, str2: str) -> str:  # type-annotated function
    '''
        Returns the concatenation of two strings

        str1: first string
        str2: second string

        return: concatenation of str1 and str2
    '''
    return str1 + str2
